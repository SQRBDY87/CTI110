import turtle as t
t.penup()

t.goto(-60,50)
t.pendown()
t.pensize(10)
t.pencolor("blue")

t.forward(10)
t.right(90)
t.forward(120)
t.circle(-40,180)
t.penup()

t.goto(-30,50)
t.pendown()
t.right(180)
t.forward(150)

t.goto(-30,50)
t.goto(20,-20)
t.goto(65,50)
t.goto(65,-100)
t.penup()










